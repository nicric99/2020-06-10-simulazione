	public List<Actor> getVertici(Map<Integer,Actor> idMap,String genere) {
		String sql = " SELECT DISTINCT(r.actor_id) AS id "
				+ "FROM movies_genres m,roles r "
				+ "WHERE genre=? AND r.movie_id=m.movie_id "
				+" ORDER BY r.actor_id ";
		List<Actor> result = new ArrayList<Actor>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, genere);
			ResultSet res = st.executeQuery();
			while (res.next()) {

				result.add(idMap.get(res.getInt("id")));
			}
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	public List<Adiacenza> getArchi(String genere,Map<Integer,Actor> idMap){
		String sql = "SELECT r1.actor_id AS a1,r2.actor_id AS a2, COUNT(*) AS conto "
				+ "FROM movies_genres m1, roles r1, movies_genres m2,roles r2 "
				+ "WHERE m1.genre=? AND r1.movie_id=m1.movie_id AND m2.genre= m1.genre AND r2.actor_id> r1.actor_id AND  "
				+ "m2.movie_id=r2.movie_id AND m1.movie_id=m2.movie_id "
				+ "GROUP BY r1.actor_id,r2.actor_id ";
		List<Adiacenza> result = new ArrayList<Adiacenza>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, genere);
			ResultSet res = st.executeQuery();
			while (res.next()) {

				result.add(new Adiacenza(idMap.get(res.getInt("a1")),idMap.get(res.getInt("a2")),res.getInt("conto")));
			}
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	
	
	
	
	
	
